char *ft_strnstr(const char *hs, const char *n, int l)
{
	int	i;
	int	i2;

	i = 0;
	i2 = 0;
	while (hs[i] != '\0' && i < l)
	{
		while (hs[i] == n[i2])
		{
			i++; 
			i2++;
			if (n[i2] == '\0')
				return ((char*)(hs + i - i2));
		}
		i2 = 0;
		i++;
	}
	return ((char*)hs);
}
